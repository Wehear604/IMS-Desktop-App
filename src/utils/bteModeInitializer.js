const sleep = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

const readMap = () => {
  try {
    return JSON.parse(localStorage.getItem(STORAGE_KEY) || "{}");
  } catch {
    return {};
  }
};

const writeMap = (map) => {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(map));
};

const makeDeviceKey = (deviceId, side) => `${String(deviceId)}:${String(side)}`;

export const hasBteModesInitialized = (deviceId, side) => {
  if (!deviceId || !side) return false;
  const map = readMap();
  return Boolean(map[makeDeviceKey(deviceId, side)]);
};

export const resetBteModesInitialized = (deviceId, side) => {
  const map = readMap();
  delete map[makeDeviceKey(deviceId, side)];
  writeMap(map);
};

export const initializeBteModesOnFirstConnection = async ({
  deviceId,
  side,
  writeData,
  packetDelayMs = 40,
}) => {
  if (!deviceId || !side || typeof writeData !== "function") {
    return { written: false, reason: "invalid-arguments" };
  }

  if (hasBteModesInitialized(deviceId, side)) {
    return { written: false, reason: "already-initialized" };
  }

  const packets = BTE_MODE_PACKETS[side];
  if (!packets?.length) {
    return { written: false, reason: "no-packets-for-side" };
  }

  for (const packet of packets) {
    await writeData(packet);
    if (packetDelayMs > 0) {
      await sleep(packetDelayMs);
    }
  }

  const map = readMap();
  map[makeDeviceKey(deviceId, side)] = true;
  writeMap(map);

  return { written: true, count: packets.length };
};
